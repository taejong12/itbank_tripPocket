package Review.dto;

public class ReviewDTO {

	
	private String reviewWriter;
	private String reviewDetail;
	private String reviewList;
	private String reviewSearch;
	private String reviewDelete;
    private String reviewModify;
    private String reviewContent;
	public String getReviewWriter() {
		return reviewWriter;
	}
	public void setReviewWriter(String reviewWriter) {
		this.reviewWriter = reviewWriter;
	}
	public String getReviewDetail() {
		return reviewDetail;
	}
	public void setReviewDetail(String reviewDetail) {
		this.reviewDetail = reviewDetail;
	}
	public String getReviewList() {
		return reviewList;
	}
	public void setReviewList(String reviewList) {
		this.reviewList = reviewList;
	}
	public String getReviewSearch() {
		return reviewSearch;
	}
	public void setReviewSearch(String reviewSearch) {
		this.reviewSearch = reviewSearch;
	}
	public String getReviewDelete() {
		return reviewDelete;
	}
	public void setReviewDelete(String reviewDelete) {
		this.reviewDelete = reviewDelete;
	}
	public String getReviewModify() {
		return reviewModify;
	}
	public void setReviewModify(String reviewModify) {
		this.reviewModify = reviewModify;
	}
	public String getReviewContent() {
		return reviewContent;
	}
	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	
}
    
    
}

